// Deep type that converts readonly literal string tuples/values to mutable string versions
type DeepStringify<T> = T extends readonly string[]
  ? string[]
  : T extends readonly (infer U)[]
    ? DeepStringify<U>[]
    : T extends object
      ? { [K in keyof T]: DeepStringify<T[K]> }
      : T extends string
        ? string
        : T;

